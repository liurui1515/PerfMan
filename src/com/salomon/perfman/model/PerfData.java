package com.salomon.perfman.model;

public class PerfData {

	private float ava = 0;
	private float min = 0;
	private float max = 0;

	public float getAva() {
		return this.ava;
	}

	public void setAva(float ava) {
		this.ava = ava;
	}

	public float getMin() {
		return this.min;
	}

	public void setMin(float min) {
		this.min = min;
	}

	public float getMax() {
		return this.max;
	}

	public void setMax(float max) {
		this.max = max;
	}

}
